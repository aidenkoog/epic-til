# 분석3: // 연산자는 바닥 나눗셈(플로어 디비전) 을 수행하는데, 이는 나눗셈 결과에서 소수점 이하를 버리고 
# 가장 작은 정수(또는 float의 경우 소수점 이하가 없는 값)를 반환
# 분석4:
# 7 / 3은 2.3333... (실수 값)을 반환
# 7 // 3은 2를 반환
